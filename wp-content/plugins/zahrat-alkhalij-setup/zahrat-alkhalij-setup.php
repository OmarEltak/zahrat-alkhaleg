/* ---------------------------------------------------------------
   Admin Notice - setup done
---------------------------------------------------------------- */
function zak_setup_admin_notice() {
    $setup_time = get_option('zak_setup_complete');
    if (!$setup_time) return;
    ?>
    <div class="notice notice-success is-dismissible">
        <p>
            <strong>زهرة الخليج:</strong>
            تم إعداد الموقع بنجاح في <?php echo esc_html($setup_time); ?>.
            يمكنك الآن <a href="<?php echo esc_url(admin_url('plugins.php')); ?>">إلغاء تفعيل وحذف هذا الملحق</a>.
        </p>
    </div>
    <?php
}
add_action('admin_notices', 'zak_setup_admin_notice');


